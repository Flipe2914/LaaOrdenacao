    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String vetorTexto = this.VetorOrdText.getText().trim();
        if (vetorTexto.isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo do vetor está vazio");
        }
        String botaoSelecionado = this.RetornaBotaoOrdenacaoSelecionado(GrupoOrdenacaoRadio);
        if (botaoSelecionado == null) {
            JOptionPane.showMessageDialog(null, "Não foi selecionado nenhum tipo de ordenação");
        }
        int[] vetorNumeros = ConverteListaStringToInt(vetorTexto.split(","));
        long inicio = System.nanoTime();
        switch (botaoSelecionado.toLowerCase()) {
            case "insertion" :                
                break;
            case "bubble":
                break;
            case "selection":
                this.ordenacao.SelectionSort(vetorNumeros);
                break;
            case "heap":
                break;
            default:
                break;
        }
        long tempoTotal = System.nanoTime()- inicio;
        this.RespostaOrdenacao.setText("");
        this.RespostaOrdenacao.setText(this.ordenacao.printaVetor(vetorNumeros)+",Tempo de execução: " + tempoTotal + "(nanosegundos)" );        
    }//GEN-LAST:event_jButton4ActionPerformed

