    public String RetornaBotaoOrdenacaoSelecionado(ButtonGroup GrupodeBotoes) {
        for (Enumeration<AbstractButton> buttons = GrupodeBotoes.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();
            if (button.isSelected()) {
                return button.getText();
            }
        }
        return null;
    }

    private int[] ConverteListaStringToInt(String[] vetorTexto) {
        List<Integer> listaDeNumeros = new ArrayList<>();
        for (String elemento : vetorTexto) {
            listaDeNumeros.add(Integer.parseInt(elemento));
        }
        int[] arrayDeNumeros = new int[listaDeNumeros.size()];
        for (int i = 0; i < listaDeNumeros.size(); i++) {
            arrayDeNumeros[i] = listaDeNumeros.get(i);
        }
        return arrayDeNumeros;
    }

